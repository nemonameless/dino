class RandomGrayscale(BaseTransform):
    def __init__(self, prob=0.2):
        super().__init__()
        self.prob = prob

    def _apply_image(self, img):
        if paddle.rand([1]) < self.prob:
            nc = len(img.split())
            return transforms.to_grayscale(img, num_output_channels=nc)
        return img


class RandomApply(BaseTransform):
    def __init__(self, transforms: list, p=0.8):
        super(RandomApply, self).__init__()
        self.p = p
        self.transforms = transforms

    def _apply_image(self, img):
        if self.p < paddle.rand([1]):
            return img
        for t in self.transforms:
            img = t(img)
        return img


class GaussianBlur(object):
    """
    Apply Gaussian Blur to the PIL image.
    """
    def __init__(self, p=0.5, radius_min=0.1, radius_max=2.):
        self.prob = p
        self.radius_min = radius_min
        self.radius_max = radius_max

    def __call__(self, img):
        do_it = random.random() <= self.prob
        if not do_it:
            return img

        return img.filter(
            ImageFilter.GaussianBlur(
                radius=random.uniform(self.radius_min, self.radius_max)
            )
        )


class Solarization(object):
    """
    Apply Solarization to the PIL image.
    """
    def __init__(self, p):
        self.p = p

    def __call__(self, img):
        if random.random() < self.p:
            return ImageOps.solarize(img)
        else:
            return img


def load_pretrained_weights(model, ckp_key, path):
    if os.path.isfile(path):
        state_dict = paddle.load(path)[ckp_key]
        new_state_dict = {k.replace("backbone.", "") : v for k, v in state_dict.items()}
        msg = model.load_dict(new_state_dict)
        print(f"Pretrained weights found at {path} and loaded with msg: {msg}")
    else:
        raise Exception(f"Weight file {path} is invalid!")


